def get_spread(ratio, spread_table):
    for lower, upper, spread in spread_table:
        if lower < ratio <= upper:
            return spread


def get_spread_backup(market_cap):
    if market_cap < 250e6:
        return 0.025
    elif 250e6 <= market_cap < 800e6:
        return 0.0175
    elif 800e6 <= market_cap < 4e9:
        return 0.01
    elif 4e9 <= market_cap < 15e9:
        return 0.008
    else:
        return 0.006


